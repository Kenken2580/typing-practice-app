/* ==== windsurf 初期設定スクリプト =========================
   - 指マッピング（JIS-109）
   - 本日の日付を自動生成した課題＋静的 9 題
   - window.windsurfConfig へ export
   ========================================================= */
(() => {
  /* ── 1. 指マッピング ───────────────────────────── */
  const fingerMap = {
    "`~1!qaz":           "left-pinky",
    '2"wsx':             "left-ring",
    "3#edc":             "left-middle",
    "4$5%rtfvgb":        "left-index",

    "6^7&yhnujm":        "right-index",
    "8*ik,<":            "right-middle",
    "9(ol.)>":           "right-ring",
    "0)-_=+p@;:[{]}\"'\\|¥": "right-pinky",

    " ":                 "thumb"
  };

  /* ── 2. 今日の日付をキーシーケンス化（課題①） ─────── */
  const todayLesson = () => {
    const d  = new Date();
    const y  = d.getFullYear();
    const m  = String(d.getMonth() + 1).padStart(2, "0");
    const dd = String(d.getDate()).padStart(2, "0");
    return ["半/全", ...`${y}/${m}/${dd}`.split("")]; // 例: ["半/全","2","0","2","5","/","0","7","/","1","3"]
  };

  /* ── 3. 静的 9 題 ────────────────────────────── */
  const staticLessons = [
    // ② 年月日（数字⇔ひらがな切替）
    ["半/全","2","0","2","5","無変換","年",
             "7","無変換","月","1","3","無変換","日"],

    // ③ URL
    ["半/全","h","t","t","p","s",":","/","/","w","w","w",".",
             "o","p","e","n","a","i",".","c","o","m","/"],

    // ④ メールアドレス
    ["半/全","u","s","e","r","_","n","a","m","e","+",
             "t","e","s","t","@",
             "e","x","a","m","p","l","e",".","c","o",".","j","p"],

    // ⑤ 電話番号
    ["半/全","0","3","-","1","2","3","4","-","5","6","7","8"],

    // ⑥ 郵便番号（〒は「ゆうびん→変換」）
    ["半/全","無変換","ゆ","う","び","ん","変換",
             "1","5","0","-","0","0","2","2"],

    // ⑦ カタカナ語（F7 全カナ）
    ["半/全","し","ょ","っ","ぴ","ん","ぐ","F7","Enter"],

    // ⑧ 英日ミックス＋感嘆符
    ["半/全","C","h","a","t","G","P","T","半/全",
             "は","す","ご","い","Shift+1"],

    // ⑨ Windows パス
    ["半/全","C",":","\\","\\","U","s","e","r","s","\\",
             "P","u","b","l","i","c","\\","D","o","c","s","\\",
             "t","e","s","t",".","t","x","t"],

    // ⑩ 顔文字
    ["半/全","Shift+9","*","^","_","^","*","Shift+0"]
  ];

  /* ── 4. windsuf へエクスポート ────────────────── */
  window.windsurfConfig = {
    fingerMap,
    lessons: [todayLesson(), ...staticLessons]
  };
})();
